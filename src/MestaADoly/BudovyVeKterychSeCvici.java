package MestaADoly;

public class BudovyVeKterychSeCvici {
    private String mesto;
    private int pozice;

    public BudovyVeKterychSeCvici(String mesto, int pozice) {
        this.mesto = mesto;
        this.pozice = pozice;
    }

    public String getMesto() {
        return mesto;
    }

    public int getPozice() {
        return pozice;
    }
}
